package com.chefgiraffe.api.services.models;

import java.util.UUID;

public class Restaurant {

    private UUID id;
    private String name;

    public Restaurant(UUID id, String name) {
        this.id = id;
        this.name = name;
    }

    public UUID getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return "Restaurant{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
